export async function getStaticPaths() {
  const { data } = await apollo.query({ query: productGQL });
  const paths = data?.products?.nodes?.map((element) => ({
    params: { slug: element?.slug },
  }));
  return {
    paths: paths,
    fallback: true,
  };
}
